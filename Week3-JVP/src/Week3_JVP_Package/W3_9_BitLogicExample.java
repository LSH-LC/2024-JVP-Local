package Week3_JVP_Package;

public class W3_9_BitLogicExample
{
	public static void main(String[] args)
	{
		int icheckvar1 = 6 & 2;
		int icheckvar2 = 6 | 2;
		int icheckvar3 = 6 ^ 2;
		int icheckvar4 = ~6;
		
		System.out.println("icheckcar1: " + icheckvar1);
		System.out.println("icheckcar2: " + icheckvar2);
		System.out.println("icheckcar3: " + icheckvar3);
		System.out.println("icheckcar4: " + icheckvar4);
	}
}
